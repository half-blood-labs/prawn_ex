  @doc """
  Returns the width of `text` in points for the given `font_name` and `font_size`.

  Uses per-character AFM widths (1000 units/em), scaled by `font_size / 1000`.
  Unknown characters fall back to 500 units (600 for Courier).

  Supported fonts: `"Helvetica"`, `"Helvetica-Bold"`, `"Times-Roman"`,
  `"Times-Bold"`, `"Courier"`. Unknown font names fall back to Helvetica metrics.
  """
  @spec text_width(String.t(), String.t(), number()) :: number()
  def text_width(font_name, text, font_size) when is_binary(text) and font_size > 0 do
    {widths, default} = font_table(font_name)

    units =
      text
      |> String.to_charlist()
      |> Enum.reduce(0, fn cp, acc -> acc + Map.get(widths, cp, default) end)

    units * font_size / 1000
  end

  def text_width(_font_name, _text, _font_size), do: 0.0

  defp font_table("Helvetica"), do: {@helvetica, 500}
  defp font_table("Helvetica-Bold"), do: {@helvetica_bold, 500}
  defp font_table("Times-Roman"), do: {@times_roman, 500}
  defp font_table("Times-Bold"), do: {@times_bold, 500}
  # Courier is monospaced — all glyphs 600 units; empty map always hits default.
  defp font_table("Courier"), do: {%{}, 600}
  defp font_table(_), do: {@helvetica, 500}
